async function updateGeographySection(selectedLanguage) {
  // Get the plain text for the selected language
  const plainText = c.geo.content[selectedLanguage];
  // Get the popovers for the selected language
  const popovers = c.geo.popover[selectedLanguage];
  // Add popovers to the text
  const finalHTML = await insertPopovers(plainText, popovers);
  // Set the updated text as the innerHTML of the geo-content element
  document.getElementById("geo-content").innerHTML = finalHTML;
  // Initialize Bootstrap popovers
  document.querySelectorAll('[data-bs-toggle="popover"]').forEach((element) => {
    new bootstrap.Popover(element);
  });
}

