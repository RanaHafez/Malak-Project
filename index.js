function generatePopoverHTML(name, info) {
  return `
    <span 
      class="city-name-li" 
      data-bs-toggle="popover" 
      data-bs-trigger="hover focus" 
      data-bs-html="true" 
      data-bs-placement="right"
      data-bs-content='<div class="popover-container">
          <svg
            class="bd-placeholder-img card-img-top"
            width="100%"
            height="225"
            xmlns="http://www.w3.org/2000/svg"
            role="img"
            aria-label="Placeholder: Thumbnail"
            preserveAspectRatio="xMidYMid slice"
            focusable="false"
          >
            <title>Placeholder</title>
            <rect width="100%" height="100%" fill="#55595c"></rect>
            <text x="50%" y="50%" fill="#eceeef" dy=".3em">${name}</text>
          </svg>
          <p>${info.replace(/"/g, "&quot;")}</p>
        </div>'>
      ${name}
    </span>`;
}

// Function to insert popovers into the text (replaces all occurrences)
async function insertPopovers(text, popovers) {
  let markedText = text;
  const markers = {};

  popovers.forEach(({ name, info }, index) => {
    const marker = `%%POPOVER_${index}%%`;
    markers[marker] = { name, info };
    markedText = markedText.replace(new RegExp(name, "g"), marker);
  });

  // Then replace markers with popover HTML
  let processedText = markedText;

  for (const [marker, { name, info }] of Object.entries(markers)) {
    const popoverHTML = await generatePopoverHTML(name, info);
    processedText = processedText.replace(new RegExp(marker, "g"), popoverHTML);
  }
  return processedText;
}
